function clearFon() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
}
 
function drawBlockage() {
    let blockageX = 0;
    let blockageY = 0;
    const dBlockageX = 20;
    const dBlockageY = 20;
    field.forEach(item => {
        item.forEach(number => {
            ctx.fillStyle = number === 1 ? 'brown' : 'green';
            ctx.fillRect(blockageX, blockageY, BLOCKAGE_WIDTH, BLOCKAGE_HEIGHT);
            blockageX += dBlockageX;
        });
        blockageX = 0;
        blockageY += dBlockageY;
    });
}
 
function Hero({
  heroX,
  heroY 
}) {
  this.x = heroX;
  this.y = heroY;
}

function drawHero(hero) {
    ctx.fillStyle = 'yellow';
    ctx.fillRect(hero.x, hero.y, HERO_WIDTH, HERO_HEIGHT);
}
 
function keyDownHandler(e) {
    if (e.keyCode === rightKeyCode) {
        rightPressed = true;
    } else if (e.keyCode === leftKeyCode) {
        leftPressed = true;
    } else if (e.keyCode === topKeyCode) {
        topPressed = true;
    } else if (e.keyCode === bottomKeyCode) {
        bottomPressed = true;
    }
}
 
function keyUpHandler(e) {
    if (e.keyCode === rightKeyCode) {
        rightPressed = false;
    } else if (e.keyCode === leftKeyCode) {
        leftPressed = false;
    } else if (e.keyCode === topKeyCode) {
        topPressed = false;
    } else if (e.keyCode === bottomKeyCode) {
        bottomPressed = false;
    }
}
 
function redraw(hero) {
    clearFon();
    drawBlockage();
    drawHero(hero);
}
 
function getHeroIndex(hero, deltaTime) {
  let heroStep = STEP * deltaTime;
  let row;
  let column;
  if (rightPressed) {
    row = Math.ceil(hero.y  / BLOCKAGE_HEIGHT);
    column = Math.ceil((hero.x + heroStep + HERO_WIDTH) / BLOCKAGE_WIDTH) - 1  
  } else if (leftPressed) {
      row = Math.ceil(hero.y  / BLOCKAGE_HEIGHT);
      column = Math.ceil((hero.x - heroStep) / BLOCKAGE_WIDTH) 
  } else if (bottomPressed) {
      row = Math.ceil((hero.y + heroStep + HERO_HEIGHT) / BLOCKAGE_HEIGHT - 1);
      column = Math.ceil(hero.x  / BLOCKAGE_WIDTH) 
  } else if (topPressed) {
      row = Math.ceil((hero.y - heroStep) / BLOCKAGE_HEIGHT);
      column = Math.ceil(hero.x  / BLOCKAGE_WIDTH)  
  } else {
      row = Math.ceil(hero.y  / BLOCKAGE_HEIGHT);
      column = Math.ceil(hero.x  / BLOCKAGE_WIDTH)
  }
  console.log(row, column);
  return {row: row, column: column};
}
 
function isCollision(hero, deltaTime) {
    let heroStep = STEP * deltaTime;
    let pos = getHeroIndex(hero, heroStep);
    if ((pos.row >= 30 ) || (pos.row < 0) || (pos.column >= 40) || (pos.column < 0)) {
        return true;
    }
    let elem = field[pos.row][pos.column];
    return elem === 1 ? true : false;
}
 
function update(hero, deltaTime) {
    let heroStep = STEP * deltaTime;
    if (isCollision(hero, deltaTime)) {
        return;
    }
    if (rightPressed) {
        hero.x += heroStep;
    } else if (leftPressed) {
        hero.x -= heroStep;
    }
    if (bottomPressed) {
        hero.y += heroStep;
    } else if (topPressed) {
        hero.y -= heroStep;
    }
}
 
function main() {
 
    document.addEventListener('keydown', keyDownHandler, false);
    document.addEventListener('keyup', keyUpHandler, false);
 
    const heroX = (canvas.width - HERO_WIDTH) / 2 + 10;
    const heroY = (canvas.height - HERO_HEIGHT) / 2 - 10;
    const hero = new Hero({heroX, heroY});
    redraw(hero);
    let lastTimestamp = Date.now(); //текущее время в ms
    const animateFn = () => {
        const currentTimeStamp = Date.now();
        const deltaTime = (currentTimeStamp - lastTimestamp) * 0.001; //сколько секунд прошло с прошлого кадра
        lastTimestamp = currentTimeStamp;
 
        update(hero, deltaTime);
        
        redraw(hero);
 
        requestAnimationFrame(animateFn);
    }
 
    animateFn();
}
 
window.onload = () => {
    main();
}